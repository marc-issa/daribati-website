function applyTranslations(lang) {
  const dict = translations[lang] || translations.en;
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (dict[key]) node.textContent = dict[key];
  });
}

function setLanguage(lang) {
  const nextLang = lang === "ar" ? "ar" : "en";
  const isArabic = nextLang === "ar";

  document.documentElement.lang = nextLang;
  document.documentElement.dir = isArabic ? "rtl" : "ltr";
  langToggle.classList.toggle("ar", isArabic);

  langOptions.forEach((opt) => {
    opt.classList.toggle("active", opt.dataset.lang === nextLang);
  });

  applyTranslations(nextLang);
  localStorage.setItem("daribati_lang", nextLang);
}

function initLanguage() {
  const saved = localStorage.getItem("daribati_lang");
  const browserLang = navigator.language.toLowerCase().startsWith("ar")
    ? "ar"
    : "en";
  setLanguage(saved || browserLang);
}

function closeMobileMenu() {
  hamburger.classList.remove("open");
  navbarLinks.classList.remove("open");
}

function setupMenu() {
  hamburger.addEventListener("click", () => {
    hamburger.classList.toggle("open");
    navbarLinks.classList.toggle("open");
  });

  navbarLinks.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", closeMobileMenu);
  });

  document.addEventListener("click", (event) => {
    if (!navbar.contains(event.target)) closeMobileMenu();
  });

  window.addEventListener("resize", () => {
    if (window.innerWidth > 767) closeMobileMenu();
  });
}

function setupScrollEffects() {
  const updateNavbar = () => {
    navbar.classList.toggle("scrolled", window.scrollY > 10);
  };

  updateNavbar();
  window.addEventListener("scroll", updateNavbar, { passive: true });

  const animatedNodes = document.querySelectorAll(".animate-on-scroll");
  if (!animatedNodes.length) return;

  const observer = new IntersectionObserver(
    (entries, obs) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) return;
        entry.target.classList.add("visible");
        obs.unobserve(entry.target);
      });
    },
    { threshold: 0.15 }
  );

  animatedNodes.forEach((node) => observer.observe(node));
}

function setupParallax() {
  const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
  const isDesktop = window.innerWidth >= 1024;
  if (reduceMotion || !isDesktop) return;
  if (!heroBg && !aboutBg) return;

  let ticking = false;
  const onScroll = () => {
    if (ticking) return;
    ticking = true;

    window.requestAnimationFrame(() => {
      const y = window.scrollY * 0.5;
      if (heroBg) heroBg.style.backgroundPositionY = `${-y}px`;
      if (aboutBg) aboutBg.style.backgroundPositionY = `${-y * 0.35}px`;
      ticking = false;
    });
  };

  window.addEventListener("scroll", onScroll, { passive: true });
  onScroll();
}

function setupLanguageToggle() {
  langToggle.addEventListener("click", () => {
    const next = document.documentElement.lang === "ar" ? "en" : "ar";
    setLanguage(next);
  });
}

function setupAnalyticsEvents() {
  if (typeof gtag !== 'function') return;

  // App Store & Google Play buttons (hero + download sections)
  document.querySelectorAll('a.btn-store').forEach(function (link) {
    var href = link.href || '';
    var eventName = href.includes('apps.apple.com')
      ? 'click_app_store'
      : href.includes('play.google.com')
        ? 'click_google_play'
        : null;
    if (eventName) {
      link.addEventListener('click', function () {
        gtag('event', eventName, { link_url: href });
      });
    }
  });

  // Phone & WhatsApp links in footer
  document.querySelectorAll('.footer a').forEach(function (link) {
    var href = link.href || '';
    if (href.startsWith('tel:')) {
      link.addEventListener('click', function () {
        gtag('event', 'click_phone', { link_url: href });
      });
    } else if (href.includes('wa.me')) {
      link.addEventListener('click', function () {
        gtag('event', 'click_whatsapp', { link_url: href });
      });
    }
  });

  // Nav links
  document.querySelectorAll('.nav-link').forEach(function (link) {
    link.addEventListener('click', function () {
      var section = link.getAttribute('href') || '';
      gtag('event', 'click_nav', { section: section.replace('#', '') });
    });
  });
}

initLanguage();
setupLanguageToggle();
setupMenu();
setupScrollEffects();
setupParallax();
setupAnalyticsEvents();
